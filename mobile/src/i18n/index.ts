/** Hook that returns a translation function `t(key)` for the current app language. */
export const useTranslation = () => {
  const language = useAppSelector(s => (s as any).settings?.language ?? 'en') as AppLanguage;
  const dict = translations[language] ?? translations.en;
  return (key: TranslationKey): string => dict[key] ?? translations.en[key] ?? key;
};

export default translations;
